export function getWatchById(id: string): Watch | undefined {
  return watches.find(w => w.id === id);
}

export function getWatchesByCategory(category: string): Watch[] {
  if (category === 'all') return watches;
  return watches.filter(w => w.category === category);
}

export function getFeaturedWatches(): Watch[] {
  return watches.filter(w => w.featured);
}

export function getBestSellers(): Watch[] {
  return watches.filter(w => w.bestSeller);
}

export function searchWatches(query: string): Watch[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return watches.filter(w =>
    w.name.toLowerCase().includes(q) ||
    w.brand.toLowerCase().includes(q) ||
    w.category.toLowerCase().includes(q) ||
    w.shortDescription.toLowerCase().includes(q) ||
    w.movement.toLowerCase().includes(q) ||
    w.specifications['Movement']?.toLowerCase().includes(q)
  );
}
